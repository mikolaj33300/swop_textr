package ui;

public class Rectangle {
    public final double startX;
    public final double startY;
    public final double width;
    public final double height;
    public Rectangle(double startX, double startY, double width, double height){
        this.startX = startX;
        this.startY = startY;
        this.width = width;
        this.height = height;
    }
}
